const str = "Привіт, світ!"; // Приклад рядка, який задовольняє умові
//const str = "Приклад рядка без літери а та містять щонайменше шість символів"; // Не задовольняє умові
const regex = /^(?!.*[Аа]).{6,}$/;

// Визначення всіх символів рядка, які задовольняють умові регулярного виразу
const matchingChars = str.match(regex);

if (matchingChars) {
    console.log("Символи, які задовольняють умові:", matchingChars[0]);
} else {
    console.log("Рядок не задовольняє умові");
}
